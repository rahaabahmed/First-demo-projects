# ---------------------Program to check even or odd number--------------------------

n = int(input("Enter a number :"))

if( n % 2 == 0):
    print("Even")
else:
    print("Odd")    



#  --------------------Program to check positive , negative or zero---------------------


N = float(input("Enter a number :"))

if( N > 0 ):
    print("Postive")
elif( N < 0 ):
    print("Negative")
else:
    print("Zero")        